// Language switching functionality for delete page
document.addEventListener('DOMContentLoaded', function() {
    const langButtons = document.querySelectorAll('.lang-btn');
    const elements = document.querySelectorAll('[data-translate]');
    
    // Set initial language
    let currentLang = 'de';
    
    function switchLanguage(lang) {
        currentLang = lang;
        
        // Update active button
        langButtons.forEach(btn => {
            btn.classList.remove('active');
            if (btn.dataset.lang === lang) {
                btn.classList.add('active');
            }
        });
        
        // Update content
        elements.forEach(element => {
            const key = element.dataset.translate;
            if (deleteTranslations[lang] && deleteTranslations[lang][key]) {
                element.textContent = deleteTranslations[lang][key];
            }
        });
        
        // Update document language
        document.documentElement.lang = lang;
        
        // Save language preference
        localStorage.setItem('delete-lang', lang);
    }
    
    // Load saved language or default to German
    const savedLang = localStorage.getItem('delete-lang') || 'de';
    switchLanguage(savedLang);
    
    // Add click handlers
    langButtons.forEach(button => {
        button.addEventListener('click', function() {
            switchLanguage(this.dataset.lang);
        });
    });
});
